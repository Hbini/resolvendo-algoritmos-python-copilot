# 2 - Repetindo Textos
# Descrição: Recebe uma string e um número, repetindo a string o número de vezes informado.

# Entrada de dados
texto = input("Digite o texto a ser repetido: ")
vezes = int(input("Quantas vezes repetir? "))

# Repetição
resultado = texto * vezes

# Saída
print(f"Resultado: {resultado}")
